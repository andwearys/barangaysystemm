    private void cmdUpdatePriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdUpdatePriceActionPerformed
        // TODO add your handling code here:
        conn=MySqlConnect.ConnectDB();
        String updateBrgyCert = "UPDATE `documentprice` SET `Price`=? WHERE `DocumentType`='Barangay Certificate'";
        String updateBrgyPermit = "UPDATE `documentprice` SET `Price`=? WHERE `DocumentType`='Barangay Permit'";
        String updateBusinessPermit = "UPDATE `documentprice` SET `Price`=? WHERE `DocumentType`='Business Permit'";
        try{
        pst=conn.prepareStatement(updateBrgyCert);
        pst.setString(1, txtBrgyCertPrice.getText());
        pst.executeUpdate();
        pst=conn.prepareStatement(updateBrgyPermit);
        pst.setString(1, txtBrgyPermitPrice.getText());
        pst.executeUpdate();
        pst=conn.prepareStatement(updateBusinessPermit);
        pst.setString(1, txtBusinessPermitPrice.getText());
        pst.executeUpdate();
        try{
            String brgyCertPrice = "select DocumentType,Price from documentprice where DocumentType='Barangay Certificate'";
            pst=conn.prepareStatement(brgyCertPrice); 
            rs=pst.executeQuery();
            while(rs.next()){
            String item = rs.getString("Price");
            txtBrgyCertPrice.setText(item);
            }
            String brgyPermitPrice = "select DocumentType,Price from documentprice where DocumentType='Barangay Permit'";
            pst=conn.prepareStatement(brgyPermitPrice); 
            rs=pst.executeQuery();
            while(rs.next()){
            String item = rs.getString("Price");
            txtBrgyPermitPrice.setText(item);
            }
            String businessPermitPrice = "select DocumentType,Price from documentprice where DocumentType='Business Permit'";
            pst=conn.prepareStatement(businessPermitPrice); 
            rs=pst.executeQuery();
            while(rs.next()){
            String item = rs.getString("Price");
            txtBusinessPermitPrice.setText(item);
            
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }conn.close();
        JOptionPane.showMessageDialog(null, "Documents Price Successfully Updated","Barangay System",JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){}
        
    }//GEN-LAST:event_cmdUpdatePriceActionPerformed

